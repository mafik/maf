#include "curve25519.hh"
#include "int.hh"

#include <gtest/gtest.h>

using namespace maf;
using namespace maf::curve25519;

TEST(Curve25519Test, PublicFromPrivate) {
  Private private_key = {.bytes = {0x20, 0x21, 0x22, 0x23, 0x24, 0x25, 0x26,
                                   0x27, 0x28, 0x29, 0x2a, 0x2b, 0x2c, 0x2d,
                                   0x2e, 0x2f, 0x30, 0x31, 0x32, 0x33, 0x34,
                                   0x35, 0x36, 0x37, 0x38, 0x39, 0x3a, 0x3b,
                                   0x3c, 0x3d, 0x3e, 0x3f}};
  Public public_key = Public::FromPrivate(private_key);

  Public expected_public_key = {
      .bytes = {0x35, 0x80, 0x72, 0xd6, 0x36, 0x58, 0x80, 0xd1,
                0xae, 0xea, 0x32, 0x9a, 0xdf, 0x91, 0x21, 0x38,
                0x38, 0x51, 0xed, 0x21, 0xa2, 0x8e, 0x3b, 0x75,
                0xe9, 0x65, 0xd0, 0xd2, 0xcd, 0x16, 0x62, 0x54}};

  EXPECT_EQ(public_key, expected_public_key);
}